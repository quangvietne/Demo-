# câu lệnh print(object , sep , end)
# sep :Phân cách giữa các đối tượng khi in (mặc định là dấu cách) 
# end :Chỉ ra kí tự đc in ở cuối dòng (mặc định là xuống dòng) 
# print : có thể dùng dấu nháy đơn ( ' ') or nháy kép (" ")
""" Đây là chú thích trên nhiều dòng ( 3 dấu nháy kép or 3 dấu nháy đơn ) """
